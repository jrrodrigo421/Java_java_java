public class DesafioDia04_01 {
    public static void main(String[] args) {
        //imprima todos os valores pares de 0 a 100;
        for (int i = 0; i <= 100; i++)
            if (i % 2 == 0)
                System.out.print(i + " ");
        System.out.println("\n");
                // imprimindo numeros impares
        for (int i = 0; i <= 100; i++)
            if (i % 2 != 0)
                System.out.print(i + " ");



    }
}

